const LANG_KEY = 'lrmj-lang';
let currentLang = 'fr';

/** Retourne la langue active */
export function getLang() { return currentLang; }

/** Applique une langue : met à jour tous les [data-i18n], <html lang>, toggle */
export function applyLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  const t = translations[lang];
  const year = new Date().getFullYear();

  document.documentElement.lang = lang === 'nl' ? 'nl' : 'fr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!t[key]) return;
    const val = t[key].replace('{year}', year);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else if (el.hasAttribute('data-i18n-html')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Sync label attr (aria-label, placeholder, alt)
  document.querySelectorAll('[data-i18n-label]').forEach(el => {
    const key = el.getAttribute('data-i18n-label');
    if (t[key]) el.setAttribute('aria-label', t[key].replace('{year}', year));
  });

  // Toggle visuel FR / NL
  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.classList.toggle('is-active', btn.dataset.lang === lang);
    btn.setAttribute('aria-current', btn.dataset.lang === lang ? 'true' : 'false');
  });

  try { localStorage.setItem(LANG_KEY, lang); } catch (_) {}
}

/** Init — lit localStorage ou navigator.language */
export function initI18n() {
  let saved;
  try { saved = localStorage.getItem(LANG_KEY); } catch (_) {}
  const nav = (navigator.language || '').toLowerCase();
  const detected = saved || (nav.startsWith('nl') ? 'nl' : 'fr');
  applyLang(detected);

  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLang(btn.dataset.lang);
    });
  });
}
